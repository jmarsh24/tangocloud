class Recording:
    def __init__(self, id, title, orchestra, singers, soloist, director, date, style, composer, author, label, lyrics, duplicates, source):
        self.id = id
        self.title = title
        self.orchestra = orchestra
        self.singers = singers
        self.soloist = soloist
        self.director = director
        self.date = date
        self.style = style
        self.composer = composer
        self.author = author
        self.label = label
        self.lyrics = lyrics
        self.duplicates = duplicates
        self.source = source